fn fips197_appendix_b_encrypt() {
    let out = aes128_encrypt(load(&FIPS197_PLAINTEXT), &FIPS197_ROUND_KEYS);
    m128i_eq(out, &FIPS197_CIPHERTEXT);
}

#[test]
fn fips197_appendix_b_decrypt() {
    let out = aes128_decrypt(load(&FIPS197_CIPHERTEXT), &FIPS197_ROUND_KEYS);
    m128i_eq(out, &FIPS197_PLAINTEXT);
}

