fn load(b: &[u8; 16]) -> __m128i {
    unsafe { _mm_loadu_si128(b.as_ptr() as *const __m128i) }
}

fn m128i_eq(a: __m128i, want: &[u8; 16]) {
    assert_eq!(u8x16(a), *want);
}

/// AES-128 encryption from the round instructions.
fn aes128_encrypt(plaintext: __m128i, keys: &[[u8; 16]; 11]) -> __m128i {
    let mut state = _mm_xor_si128(plaintext, load(&keys[0]));
    for k in keys.iter().take(10).skip(1) {
        state = _mm_aesenc_si128(state, load(k));
    }
    _mm_aesenclast_si128(state, load(&keys[10]))
}

/// AES-128 decryption from the round instructions.
fn aes128_decrypt(ciphertext: __m128i, keys: &[[u8; 16]; 11]) -> __m128i {
    let mut dec = [_mm_setzero_si128(); 11];
    dec[0] = load(&keys[10]);
    for i in 1..=9 {
        dec[i] = _mm_aesimc_si128(load(&keys[10 - i]));
    }
    dec[10] = load(&keys[0]);
    let mut state = _mm_xor_si128(ciphertext, dec[0]);
    for k in dec.iter().take(10).skip(1) {
        state = _mm_aesdec_si128(state, *k);
    }
    _mm_aesdeclast_si128(state, dec[10])
}

