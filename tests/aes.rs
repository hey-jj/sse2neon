#[test]
fn roundtrip_zero_and_ones() {
    for pt in [[0u8; 16], [0xFFu8; 16]] {
        let p = load(&pt);
        let c = aes128_encrypt(p, &FIPS197_ROUND_KEYS);
        let back = aes128_decrypt(c, &FIPS197_ROUND_KEYS);
        assert_eq!(u8x16(back), pt);
    }
}

#[test]
fn aesimc_is_inverse_of_mixcolumns() {
    // aesenc without the key, stripped of the affine parts, is hard to isolate.
    // Instead check aesimc composed with the mix inside aesdec round-trips a
    // known state: aesimc(aesimc-free path) is validated through decrypt above.
    // Here assert aesimc changes data and is deterministic.
    let a = load(&NIST_C1_PLAINTEXT);
    let r1 = _mm_aesimc_si128(a);
    let r2 = _mm_aesimc_si128(a);
    assert_eq!(u8x16(r1), u8x16(r2));
    assert_ne!(u8x16(r1), u8x16(a));
}

#[test]
fn aesenclast_differs_from_aesenc() {
    let a = load(&NIST_C1_PLAINTEXT);
    let k = load(&NIST_C1_ROUND_KEYS[1]);
    assert_ne!(
        u8x16(_mm_aesenc_si128(a, k)),
        u8x16(_mm_aesenclast_si128(a, k))
    );
}

#[test]
fn keygenassist_known_answer() {
    // Input word X1=X3=0x00000000. SubWord(0)=0x63636363.
    // RotWord(0x63636363)=0x63636363, xor RCON. With RCON=1, lanes 1 and 3 get
    // 0x63636363 ^ 1 = 0x63636362.
    let a = _mm_setzero_si128();
    let r = u32x4(_mm_aeskeygenassist_si128::<0x01>(a));
    assert_eq!(r[0], 0x6363_6363);
    assert_eq!(r[1], 0x6363_6362);
    assert_eq!(r[2], 0x6363_6363);
    assert_eq!(r[3], 0x6363_6362);
    // RCON 0 leaves the rotated words unchanged.
    let r0 = u32x4(_mm_aeskeygenassist_si128::<0x00>(a));
    assert_eq!(r0[1], 0x6363_6363);
}

#[test]
fn clmul_low_low() {
    // 0b11 * 0b11 carry-less = 0b101 (x^2 + 1) since (x+1)(x+1)=x^2+1 in GF(2).
    let a = _mm_set_epi64x(0, 0b11);
    let b = _mm_set_epi64x(0, 0b11);
    let r = u64x2(_mm_clmulepi64_si128::<0x00>(a, b));
    assert_eq!(r[0], 0b101);
    assert_eq!(r[1], 0);
}

#[test]
fn keygenassist_nonzero_word_and_rotation() {
    // Seed word 0x09cf4f3c in dword 3 (bytes 12..15), other bytes zero.
    let a = _mm_set_epi32(0x09cf_4f3c, 0, 0, 0);
    let r = u32x4(_mm_aeskeygenassist_si128::<0x01>(a));
    // SubWord(0x09cf4f3c) = 0x018a84eb.
    assert_eq!(r[2], 0x018a_84eb);
    // RotWord moves each byte down one position: rotate_right(8) on the dword.
    // 0x018a84eb.rotate_right(8) = 0xeb018a84, xor rcon 1 = 0xeb018a85.
    assert_eq!(r[3], 0xeb01_8a85);
}

#[test]
fn clmul_immediate_selects_halves() {
    let a = _mm_set_epi64x(0xA, 0x3); // high lane 0xA, low lane 0x3
    let b = _mm_set_epi64x(0xC, 0x5);
    // 0x00: low a * low b = clmul(3, 5) = 0xF.
    assert_eq!(u64x2(_mm_clmulepi64_si128::<0x00>(a, b)), [0xF, 0]);
    // 0x01: high a * low b = clmul(0xA, 5) = 0x22.
    assert_eq!(u64x2(_mm_clmulepi64_si128::<0x01>(a, b)), [0x22, 0]);
    // 0x10: low a * high b = clmul(3, 0xC) = 0x14.
    assert_eq!(u64x2(_mm_clmulepi64_si128::<0x10>(a, b)), [0x14, 0]);
    // 0x11: high a * high b = clmul(0xA, 0xC) = 0x78.
    assert_eq!(u64x2(_mm_clmulepi64_si128::<0x11>(a, b)), [0x78, 0]);
}

#[test]
fn clmul_high_output_lane() {
    // clmul of two all-ones 64-bit values fills both output lanes.
    let w = _mm_set_epi64x(0, 0xFFFF_FFFF_FFFF_FFFFu64 as i64);
    let r = u64x2(_mm_clmulepi64_si128::<0x00>(w, w));
    assert_eq!(r, [0x5555_5555_5555_5555, 0x5555_5555_5555_5555]);
}
